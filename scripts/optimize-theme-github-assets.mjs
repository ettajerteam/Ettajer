import sharp from "sharp";
import fs from "fs/promises";
import path from "path";

const THEMES_DIR = path.join(process.cwd(), "public", "assets", "themes");
const BLUR_OUT = path.join(process.cwd(), "lib", "theme-blur-data.ts");

const FILES = [
  "minimal-preview",
  "minimal-hero",
  "modern-preview",
  "modern-hero",
  "bold-preview",
  "bold-hero",
];

async function main() {
  const blurEntries = [];

  for (const name of FILES) {
    const png = path.join(THEMES_DIR, `${name}.png`);
    const webp = path.join(THEMES_DIR, `${name}.webp`);

    try {
      await fs.access(png);
    } catch {
      console.warn(`skip ${name}: no PNG`);
      continue;
    }

    const isPreview = name.includes("preview");
    await sharp(png)
      .rotate()
      .resize(isPreview ? 800 : 1200, isPreview ? 600 : 675, { fit: "cover", position: "centre" })
      .webp({ quality: 82, effort: 4 })
      .toFile(webp);

    const tiny = await sharp(png)
      .resize(12, 9, { fit: "cover" })
      .webp({ quality: 40 })
      .toBuffer();

    blurEntries.push(`  "${name}": "data:image/webp;base64,${tiny.toString("base64")}",`);
    console.log(`✓ ${name}.webp`);
  }

  const content = `/** Auto-generated by scripts/optimize-theme-github-assets.mjs */\nexport const THEME_BLUR_DATA: Record<string, string> = {\n${blurEntries.join("\n")}\n};\n\nexport function getThemePreviewBlur(themeId: string): string | undefined {\n  return THEME_BLUR_DATA[\`\${themeId}-preview\`];\n}\n`;
  await fs.writeFile(BLUR_OUT, content, "utf8");
  console.log(`\nWrote ${BLUR_OUT}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
